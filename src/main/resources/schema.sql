drop table category if exists;
drop table ingredient if exists;
drop table notes if exists;
drop table recipe if exists;
drop table recipe_category if exists;
drop table unit_of_measure if exists;
create table category (id bigint generated by default as identity, description varchar(255), primary key (id));
create table ingredient (id bigint generated by default as identity, amount decimal(19,2), description varchar(255), recipe_id bigint, uom_id bigint, primary key (id));
create table notes (id bigint generated by default as identity, recipe_notes clob, recipe_id bigint, primary key (id));
create table recipe (id bigint generated by default as identity, cook_time integer, description varchar(255), difficulty varchar(255), directions clob, image blob, prep_time integer, servings integer, source varchar(255), url varchar(255), notes_id bigint, primary key (id));
create table recipe_category (recipe_id bigint not null, category_id bigint not null, primary key (recipe_id, category_id));
create table unit_of_measure (id bigint generated by default as identity, description varchar(255), primary key (id));
alter table ingredient add constraint FK_ingredient_recipe foreign key (recipe_id) references recipe;
alter table ingredient add constraint FK_ingredient_UOM foreign key (uom_id) references unit_of_measure;
alter table notes add constraint FK_note_recipe foreign key (recipe_id) references recipe;
alter table recipe add constraint FK_recipe_note foreign key (notes_id) references notes;
alter table recipe_category add constraint FK_category foreign key (category_id) references category;
alter table recipe_category add constraint FK_recipe foreign key (recipe_id) references recipe;